   public void affiche()
    { 
        
        DefaultTableModel tm = (DefaultTableModel) tableRC.getModel();
      
        List<ligneCommande> services=new ArrayList<ligneCommande>();
       services=ligneCommandeDAO.getInstance().readAll();
       for (int i = 0; i < services.size(); i++)
       {
       tm.addRow(new Object[] {services.get(i).getCommande(),services.get(i).getProduit(),services.get(i).getQuantite(),services.get(i).getPrix()});
       }
       tm.addRow(new Object[] {null,null,null,null});
    }
