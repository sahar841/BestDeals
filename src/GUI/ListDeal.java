     public void add()
    {
           DefaultTableModel tm = (DefaultTableModel) tableLD.getModel();
       
       
        List<Deal> services=new ArrayList<Deal>();
       services=DealDAO.getInstance().readAll();
       for (int i = 0; i < services.size(); i++)
       {
    tm.addRow(new Object[] {services.get(i).getDescription(),services.get(i).getDateCreation(),services.get(i).getDateLimit(),services.get(i).getNom(),services.get(i).getType(),services.get(i).getResponsableSociete().getNom(),services.get(i).getCatalogue().getNom()});
       
       }
        
    }
